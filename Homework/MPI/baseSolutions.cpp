SolutionMatrix getStretchedSolutionV(int n, int m) {
    return knightTourSolutionsStretchedV[{n, m}];
}

SolutionMatrix getStretchedSolutionH(int n, int m) {
    return knightTourSolutionsStretchedH[{n, m}];
}

SolutionMatrix getDoubleLoopSolution(int n, int m) {
    return knightTourSolutionsDoubleLoop[{n, m}];
}

SolutionMatrix getOpenSolution(int n, int m) {
    if ( !knightTourSolutionsOpen[{n, m}].empty()){
        return knightTourSolutionsOpen[{n, m}];
    }
    else {
        if (getStretchedSolutionH(n, m).empty()) {
            return getStretchedSolutionV(n, m);
        }
        else {
            return getStretchedSolutionH(n, m);
        }
    }
}

